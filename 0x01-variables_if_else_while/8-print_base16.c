#include <stdio.h>

/**
 * Description: A program that prints all the numbers of base 16 in lowercase
 * 
 * Return: always 0 (success)
 *
 */
int main(void)

{
	char digit;

	for (digit = '0'; digit <= '0'; digit++)
	putchar(digit);

	for (digit = 'a'; digit <= 'f'; digit++)
	putchar(digit);

	putchar('\n');

	return (0);
}
